<?php

    // 산술연산자, 할당연산자

    // 사칙연산
    echo (5 + 4)."<br>"; # 더하기
    echo (5 - 4)."<br>"; # 빼기
    echo (5 * 4)."<br>"; # 곱하기
    echo (400 / 4)."<br>"; # 나누기
    echo (7 % 4)."<br>"; # 나머지
    echo (2 ** 3)."<br>"; # 제곱

    // 할당연산 (=) 들어가면
    $num1 = 5;
    $num2 = $num1;
    echo $num2."<br>";

    $num2 += 5; # $num = $num + 5 (+=, -=, *=, /=, %=) 다 가능
    echo $num2."<br>"; # 10

?>